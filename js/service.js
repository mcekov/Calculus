  function calc(ev) {
    ev.preventDefault();

    let calcForm = getFormData($('#calcForm'));
    console.log(calcForm);
    let country = $('#countrySelect option:selected').text();

    // let price = getPrice(Number(calcForm.weight), Number(calcForm.zone));
    let zone = Number(calcForm.zone);
    let price = getPrice(calcForm.weight, zone, calcForm.tariff);
    console.log(price);

    // Tax Fuel
    let taxFuel = ((price * Number(calcForm.fuelTax)) / 100).toFixed(2);

    // Discount %
    let discount = ((price * Number(calcForm.discount)) / 100).toFixed(2);

    // Total
    let totalResult = (price - Number(discount) + Number(taxFuel)).toFixed(2);

    createResult([
      country,
      calcForm.zone,
      price,
      taxFuel,
      discount,
      totalResult
    ]);
  }

